# frozen_string_literal: true

class Dashboard::ApiKeysController < ApplicationController
  before_action :authenticate_user!
  before_action :set_api_key, only: [ :regenerate, :revoke ]
  layout "dashboard"

  def index
    @api_keys = current_user.api_keys.order(created_at: :desc)
    @active_keys = @api_keys.active_keys
    @revoked_keys = @api_keys.revoked
  end

  def new
    @api_key = current_user.api_keys.build
  end

  def create
    @api_key = current_user.api_keys.build(api_key_params)

    if @api_key.save
      # The full key is only available once during creation
      @show_full_key = true
      flash.now[:notice] = "API key created successfully. Make sure to copy it now - you won't be able to see it again!"
      render :show_key
    else
      render :new, status: :unprocessable_entity
    end
  end

  def regenerate
    if @api_key.revoked_at.present?
      redirect_to dashboard_api_keys_path, alert: "Cannot regenerate a revoked key. Please create a new one."
      return
    end

    # Revoke old key
    @api_key.revoke!(reason: "Regenerated by user")

    # Create new key with same name
    @new_api_key = current_user.api_keys.create!(
      name: @api_key.name,
      environment: @api_key.environment
    )

    @show_full_key = true
    @api_key = @new_api_key
    flash.now[:notice] = "API key regenerated successfully. Make sure to copy the new key - you won't be able to see it again!"
    render :show_key
  end

  def revoke
    if @api_key.revoke!(reason: "Revoked by user")
      redirect_to dashboard_api_keys_path, notice: "API key revoked successfully."
    else
      redirect_to dashboard_api_keys_path, alert: "Failed to revoke API key."
    end
  end

  private

  def set_api_key
    @api_key = current_user.api_keys.find(params[:id])
  rescue ActiveRecord::RecordNotFound
    redirect_to dashboard_api_keys_path, alert: "API key not found."
  end

  def api_key_params
    params.require(:api_key).permit(:name, :environment)
  end
end
